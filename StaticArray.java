package batch11_CodeBase;
public class StaticArray {
    public static void main(String[] args) {
        // Fixed size - cannot change
        int[] arr = {10, 20, 30, 40, 50};

        // Print all elements
        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i]);
        }
    }
}